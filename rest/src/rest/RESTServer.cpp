void zapata::RESTServer::start() {
	try {
		if (this->__type == "PUB/SUB") {
			zapata::Job _proxy([ & ] (zapata::Job& _job) -> void {
				((zapata::ZMQXPubXSub *) this->__assync.get())->loop();
			});
			_proxy->start();
		}
		this->__poll->loop();
	}
	catch (zapata::InterruptedException& e) {
		return;
	}
}

